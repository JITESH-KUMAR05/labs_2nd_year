package Area ;
public class Rectangles {
    public double  area(double l, double b){
        return l * b;
        
    }
    
}
public class Circle {
    public double area(double r){
        return 3.14 * r * r;
        
    }
    
}
public class square {
    public double  area(double s){
        return s * s;
        
    }
}